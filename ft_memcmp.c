#include "libft.h"

int	ft_memcmp(void *s1, void *s2, size_t n)
{
	size_t	cont;

	cont = 0;
	while ((s1 || s2) && (cont < n))
	{
		if (*(unsigned char *)s1 != *(unsigned char *)s2)
			return (*(unsigned char *)s1 - *(unsigned char *)s2);
		s1++;
		s2++;
		cont++;
	}
	return (0);
}
